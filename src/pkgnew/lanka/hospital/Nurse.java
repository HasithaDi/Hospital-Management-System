    private void countDoctor(){
        try {
            
            st = conn.createStatement();
            Rs = st.executeQuery("SELECT MAX(ID) FROM nurse");
            Rs.next();
            NurseId = Rs.getInt(1)+1;
            
        } catch (Exception e) {
        }
    }
    
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        // TODO add your handling code here:
        if(NNameTB.getText().isEmpty() || NAddressTB.getText().isEmpty() || NContactTB.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Data!!!");
        }else{
            try {
                countDoctor();
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/new lanka hospital", "Hasitha", "1234");
                PreparedStatement save = conn.prepareStatement("INSERT INTO `nurse` (`Name`, `Address`, `Contact`) VALUES(?, ?, ?)"); 
                
                save.setInt(1, NurseId);
                save.setString(1, NNameTB.getText().toString());
                save.setString(2, NAddressTB.getText().toString());
                save.setString(3, NContactTB.getText().toString());
              
                int row = save.executeUpdate();
                JOptionPane.showMessageDialog(this, "Nurse Add !!!");
                conn.close();
                showNurse();
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_AddBtnMouseClicked

